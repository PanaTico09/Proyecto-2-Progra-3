    private void botonCargarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCargarTodosActionPerformed
        try {
            List<DatosAlgoritmos> recuperarTodas = this.datos_servicio.recuperarTodas(Conexion.obtener());
            if (recuperarTodas.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay datos guardados.");
                return;
            }
            DefaultTableModel modelo = (DefaultTableModel) tablaDatos.getModel();
            Object[] Columna = new Object[modelo.getColumnCount()];
            while (modelo.getRowCount() > 0) {
                modelo.removeRow(0);
            }
            for (int i = 0; i < recuperarTodas.size(); i++) {
                Columna[0] = recuperarTodas.get(i).getId_algoritmo();
                Columna[1] = recuperarTodas.get(i).getNombreAlgoritmo();
                Columna[2] = recuperarTodas.get(i).getTipoEstructura();
                Columna[3] = recuperarTodas.get(i).getFecha();
                Columna[4] = recuperarTodas.get(i).getDuracion();
                modelo.addRow(Columna);
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido cargar los registros.");
        } catch (ClassNotFoundException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido cargar los registros.");
        }

    }//GEN-LAST:event_botonCargarTodosActionPerformed

